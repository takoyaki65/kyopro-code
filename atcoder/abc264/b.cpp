#include <iostream>
using namespace std;
const char grid[15][16] = {
    "***************", "*.............*", "*.***********.*", "*.*.........*.*",
    "*.*.*******.*.*", "*.*.*.....*.*.*", "*.*.*.***.*.*.*", "*.*.*.*.*.*.*.*",
    "*.*.*.***.*.*.*", "*.*.*.....*.*.*", "*.*.*******.*.*", "*.*.........*.*",
    "*.***********.*", "*.............*", "***************"};
int main() {
  int r, c;
  cin >> r >> c;
  if (grid[r - 1][c - 1] == '*')
    cout << "black" << endl;
  else
    cout << "white" << endl;
  return 0;
}